CREATE TABLE login (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    email TEXT NOT NULL,
    password TEXT NOT NULL
);


CREATE TABLE todo_chart (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
     title TEXT NOT NULL,
    summary TEXT NOT NULL,
    date TEXT NOT NULL,
    login INTEGER REFERENCES login(email) NOT NULL
);

CREATE TABLE add_todo (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title TEXT NOT NULL,
    summary TEXT NOT NULL,
    date TEXT NOT NULL,
    todo_chart INTEGER REFERENCES todo_chart(id)  NOT NULL
);